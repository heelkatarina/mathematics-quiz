<?php

// Example variable declaration
$variable1 = 5;
$variable2 = "Hello World";
$variable3 = null;

// Conditional statement
if ($variable1 > $variable2) {
    echo "The first number is greater than the second number.";
} else {
    echo "The first number is not greater than the second number.";
}

// Execute basic arithmetic operations

// Perform addition
additionResult = 5 + 3;
echo "The sum of 5 and 3 is: $additionResult";

// Compare two numbers
comparisonResult = 5 == 4 || 2 < 10;
echo "Is the number 5 equal to 4? " . ($comparisonResult ? 'Yes' : 'No');

// Use an array function

$fruits = ['apple', 'banana', 'orange'];
sort($fruits);
print_r($fruits);

// Execute another conditional statement
if ($variable3 !== null) {
    echo "The variable is not null.";
} else {
    echo "The variable is null.";
}

// Example of a loop

for ($i = 0; $i < 10; $i++) {
    echo "Iteration: $i";
}
